package AnalizadorLexico;

/**
 *
 * @author br340
 */
public class TABSIM {
    String var;
    String valor;
    int tipo;
    int id;

    public TABSIM(String var, String valor, int tipo, int id) {
        this.var = var;
        this.valor = valor;
        this.tipo = tipo;
        this.id = id;
    }

    public String getVar() {
        return var;
    }

    public void setVar(String var) {
        this.var = var;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
}
